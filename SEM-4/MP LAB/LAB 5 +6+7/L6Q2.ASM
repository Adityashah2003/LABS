DATA SEGMENT
NUM DW 00015H,00018H,00021H,0012H
GCD DW ?
DATA ENDS

CODE SEGMENT
ASSUME CS:CODE, DS: DATA

HCF PROC
AGAIN : 
	CMP AX,BX
	JE EXIT
	JB BIGBX
DIVAXBX:
	MOV DX,0
	DIV BX
	CMP DX,0
	JE EXIT
	MOV AX,DX
	JMP AGAIN
BIGBX:
	XCHG AX,BX
	JMP DIVAXBX
EXIT:
	MOV GCD,BX
	
RET
HCF ENDP

START:
MOV AX,DATA
MOV DS,AX
MOV AX,NUM
MOV BX,NUM+2
CALL HCF
MOV AX,GCD
MOV BX,NUM+4
CALL HCF
MOV AX,GCD
MOV BX,NUM+6
CALL HCF

MOV AH, 4CH
INT 21H
CODE ENDS
END START
