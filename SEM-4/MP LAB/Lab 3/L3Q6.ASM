DATA SEGMENT
NUM1 DB 10 DUP(0FFH)
NUM2 DB 10 DUP(11H)
RES DB 10 DUP(0H)

DATA ENDS

CODE SEGMENT
ASSUME CS:CODE,DS:DATA

START: 
	MOV AX, DATA
	MOV DS, AX
	MOV CX,0AH;MOV BP,OFFSET RES
	
	LEA BP,RES
	LEA SI,NUM1
	LEA DI,NUM2
	CLC

NEXT:
	MOV AX,[SI];
	SBB AX,[DI];

	MOV DS:[BP],AX
	INC DI
	INC SI
	INC BP
	LOOP NEXT

MOV AH, 4CH 
INT 21H
CODE ENDS
END START