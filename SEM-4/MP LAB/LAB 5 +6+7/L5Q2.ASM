DATA SEGMENT
HEX DB 0ADh
DIVISOR DB 0Ah
BCD DB 00h
DATA ENDS

CODE SEGMENT
ASSUME CS: CODE, DS: DATA
START:
MOV AX, DATA
MOV DS, AX
MOV AX, 00h
MOV AL, HEX
LEA SI, BCD
L1:
    DIV AL, DIVISOR
    MOV [SI], AH
    INC SI
    CMP AL, DIVISOR
    MOV AH, 00h
    JAE L1
MOV [SI], AL
MOV AH, 4Ch
INT 21h
CODE ENDS
END START
